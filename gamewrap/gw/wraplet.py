import shlex
 
class Wraplet():
    
    def __init__(self, filename):
        self.filename = filename
        
        fd = open(filename, "r")
        try:
            lexr = shlex.shlex()
            lexr.wordchars = "._"
        finally:
            fd.close()
        
        